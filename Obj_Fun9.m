function y = Obj_Fun9(x)

%     y(1) = 7*(x(1).^2 + x(2).^2) +8* sin(x(1).^2 + x(2).^2);
%     y(2) =9* (3*x(1) - 2*x(2) + 4).^2/8 + (x(1) - x(2) + 1).^2/27 + 15;
%     y(3) = 1/(x(1).^2 + x(2).^2 + 1) - 1.1*exp(-(x(1).^2 + x(2).^2));

L=[0.3722 	0.7222 	0.5556 	0.8167 	0.7389 	0.1389;
0.3655 	0.4726 	0.4524 	0.5476 	0.4750 	0.2857;
0.7792 	0.9167 	0.9167 	0.5292 	0.6375 	0.6125;
0.6111 	0.2056 	0.8167 	0.2611 	0.5944 	0.9444;
0.4733 	0.6167 	0.4467 	0.7400 	0.7067 	0.3900];

M=[0.709938306	0.842356751	0.838972234	0.532241718	0.610311527	0.55782189;
0.51044033	0.315010321	0.667374046	0.378547171	0.545486181	0.674465813];

W=[0.1796 	0.2608 	0.1641 	0.1710 	0.2244];

F(1,:) = (1-x(1))*L(1,:)+x(1)*L(5,:);
F(2,:) = (1-x(2))*L(2,:)+x(2)*L(4,:);
F(3,:) = (1-x(3))*L(3,:)+x(3)*L(2,:);
F(4,:) = (1-x(4))*L(4,:)+x(4)*L(2,:);
F(5,:) = (1-x(5))*L(5,:)+x(5)*L(4,:);

G(1,:)=W(1)*F(1,:)+W(2)*F(2,:)+W(3)*F(3,:)+W(4)*F(4,:)+W(5)*F(5,:);

E(1,:)=abs(F(1,:)-L(1,:));
E(2,:)=abs(F(2,:)-L(2,:));
E(3,:)=abs(F(3,:)-L(3,:));
E(4,:)=abs(F(4,:)-L(4,:));
E(5,:)=abs(F(5,:)-L(5,:));

z=[];

for i=1:1:5
    if i == 3 
         M_min(i-2,:) = min(M(1,:),L(i,:));
         M_max(i-2,:) = max(M(1,:),L(i,:));
            if x(i) >= (M_min(i-2,:)-L(i,:))/(L(2,:)-L(i,:)) && x(i) <= (M_max(i-2,:)-L(i,:))/(L(2,:)-L(i,:))
                z(i)=0;
            else
                D(i-2,:)=abs((1-x(i))*L(i,:)+x(i)*L(2,:)-M(i-2,:))
                U(1,:)=-1*(D(i-2,:)).^0.88;
                z(i)=sum(U(1,:),2);           
            end
    elseif i == 4
        M_min(i-2,:) = min(M(i-2,:),L(i,:));
        M_max(i-2,:) = max(M(i-2,:),L(i,:));
            if x(i) >= (M_min(i-2,:)-L(i,:))/(L(2,:)-L(i,:)) && x(i) <= (M_max(i-2,:)-L(i,:))/(L(2,:)-L(i,:))
                z(i)=0;
            else
                D(i-2,:)=abs((1-x(i))*L(i,:)+x(i)*L(2,:)-M(i-2,:))
                U(2,:)=-1*(D(i-2,:)).^0.88;
                z(i)=sum(U(2,:),2);
            end
    else
        z(i) = 0;
    end
end

y(1) = sum(E(:))/30; 
y(2) = -(z(1)+z(2)+0.54*z(3)+0.46*z(4)+z(5))/6;

end
